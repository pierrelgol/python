# Bitwise Operators

val1 = 10
val2 = 5

print("----- Bitwise operators -----\n")

print(f"val1 = {val1}, val2 = {val2}")
print("")

print("Bitwise '&' performs binary AND")
print(f"0 & 0 = {0 & 0}")
print(f"0 & 1 = {0 & 1}")
print(f"1 & 0 = {1 & 0}")
print(f"1 & 1 = {1 & 1}")
print(f"val1 & val2 = {val1 & val2}")
print("")

print("Bitwise '|' performs binary OR")
print(f"0 | 0 = {0 | 0}")
print(f"0 | 1 = {0 | 1}")
print(f"1 | 0 = {1 | 0}")
print(f"1 | 1 = {1 | 1}")
print(f"val1 | val2 = {val1 | val2}")
print("")

print("Bitwise '^' performs binary XOR")
print(f"0 ^ 0 = {0 ^ 0}")
print(f"0 ^ 1 = {0 ^ 1}")
print(f"1 ^ 0 = {1 ^ 0}")
print(f"1 ^ 1 = {1 ^ 1}")
print(f"val1 ^ val2 = {val1 ^ val2}")
print("")

print("Bitwise '~' performs binary NOT")
print(f"~0 = {~0}")
print(f"~1 = {~1}")
print(f"~val1 = {~val1}")
print("")

print("Bitwise '<<' shift left operation")
print(f"2 << 1 = {2 << 1}")
print("")
print("Bitwise '>>' shift right operation")
print(f"2 >> 1 = {2 >> 1}")
print("")
