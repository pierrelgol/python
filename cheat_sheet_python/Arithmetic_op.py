# Arithmetic Operators

print("----- Arithmetic operators -----\n")

add = 1 + 1
sub = 2 - 1
mul = 2 * 2
div = 4 / 2
rem = 4 % 3
exp = 4 ** 2
fdv = 5 // 2

print(f"Addition: 1 + 1 = {add}")
print(f"Subtraction: 2 - 1 = {sub}")
print(f"Multiplication: 2 * 2 = {mul}")
print(f"Division: 4 / 2 = {div}")
print(f"Remainder: 4 % 3 = {rem}")
print(f"Exponentiation: 4 ** 2 = {exp}")
print(f"Floor Division: 5 // 2 = {fdv}")
