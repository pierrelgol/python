# Assignment Operators

add = 1 + 1
sub = 2 - 1
mul = 2 * 2
div = 4 / 2
rem = 4 % 3
exp = 4 ** 2
fdv = 5 // 2

print("----- Assignment operators -----\n")

add += 1
sub -= 1
mul *= 2
div /= 2
rem %= 3
exp **= 2
fdv //= 2

print(f"Updated values:")
print(f"Addition: add += 1 => {add}")
print(f"Subtraction: sub -= 1 => {sub}")
print(f"Multiplication: mul *= 2 => {mul}")
print(f"Division: div /= 2 => {div}")
print(f"Remainder: rem %= 3 => {rem}")
print(f"Exponentiation: exp **= 2 => {exp}")
print(f"Floor Division: fdv //= 2 => {fdv}")
